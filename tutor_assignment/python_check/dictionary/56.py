# 중첩 딕셔너리를 만드시오

# 중첩 딕셔너리란 딕셔너리 안에 또 다른 딕셔너리를 값으로 가지는 구조

store_items = {'fruits': {'apple': 2,'banana': 1},
               'vegetables': {'carrot': 3,'lettuce': 2}}

print(store_items['fruits']['apple'])      # 과일 키에서 애플 키의 값을 보여줘. 출력 : 2
print(store_items['vegetables']['carrot']) # 채소 키에서 당근 키의 값을 보여줘. 출력: 3
